#include<iostream>
using namespace std;

int main (){

    int p=10, q=15;

    // ==operator
    cout<<(p==q)<<endl;

    // !=operator
    cout<<(p!=q)<<endl;

    // greater than
    cout<<(p>q)<<endl;

    // less than
    cout<<(p<q)<<endl;

    // greater than equal

    cout<<(p>=q)<<endl;

    // less than equal
    cout<<(p<=q)<<endl;
    
    
return 0;

}