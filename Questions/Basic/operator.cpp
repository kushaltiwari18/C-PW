#include <iostream>
using namespace std;

int main()
{

    int p = 20, q = 10;

    // Addition

    cout << "Sum : " << p + q << endl;

    // Subtraction

    cout << "Sub : " << p - q << endl;

    //  Multiplication

    cout << "Multi : " << p * q << endl;

    //  Division

    cout << "Division : " << p / q << endl;

    // Modulus

    cout << "Modulus : " << p % q << endl;

    return 0;
}